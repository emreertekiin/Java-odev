    private void araActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_araActionPerformed
        DefaultTableModel tablo = (DefaultTableModel)kitaplarTable.getModel();
        int rows = tablo.getRowCount();
        String ara = aramacubuk.getText();
        Connection c;
        Statement st ;
        ResultSet rs ;
        for(int i=rows-1;i>=0;i--)
       
        {
            tablo.removeRow(i);
        }
        try
        {
          
            Class.forName("org.sqlite.JDBC");
            c= DriverManager.getConnection("jdbc:sqlite:c:\\Kutuphane\\kutuphane.sqlite");
            st=c.createStatement();
            String query ="SELECT * FROM Kitaplar where isim like '%"+ ara +"%' ";
            rs=st.executeQuery(query);
            while(rs.next())
            {
                String idal = rs.getString("Id");
                String isimal = rs.getString("isim");
                String baskial = rs.getString("baski");
                String yayimcial = rs.getString("yayimci");
                String sayfaal = rs.getString("sayfasayisi");
                String stokal = rs.getString("stok");
                
                tablo.addRow(new Object[] {idal,isimal,baskial,yayimcial,sayfaal,stokal});
            }
            
          
        }
        catch(Exception e)
        {
            
        }    
        
    }//GEN-LAST:event_araActionPerformed

